package basicSyntax;

public class Variables {

	public static void main(String[] args) {
		String subject = "私の";
		String adjective = "好きな";
		String verb ="です。";
	    
	    int num1;
	    double num2;
	    
	    num1= 1;
	    num2= 0.2;
	    
	    System.out.println(num1);
	    System.out.println(num2);
	    System.out.println(subject + adjective +  "整数は" + num1 + verb);
	    System.out.println(subject + adjective +  "少数は" + num2 + verb);
	}
}
